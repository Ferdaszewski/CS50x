#ifndef DICTIONARY_H
#define DICTIONARY_H

#include <stdbool.h>

// maximum length for a word
// (e.g., pneumonoultramicroscopicsilicovolcanoconiosis)
#define LENGTH 45

// lenght of alphabet + any additional characters
// a to z plus apostrophy in this case
#define SIZE 27

// node for tries data structure to hold dictionary in memory (a-z + ')
typedef struct node
{
    bool isword;
    struct node* children[SIZE];
}node;

/**
 * alocates and returns a new node in the tries data structure
 */
struct node* nalloc(void);

/**
 * Returns true if word is in dictionary else false.
 */
bool check(const char* word);

/**
 * Loads dictionary into memory.  Returns true if successful else false.
 */
bool load(const char* dictionary);

/**
 * Returns number of words in dictionary if loaded else 0 if not yet loaded.
 */
unsigned int size(void);

/**
 * Unloads dictionary from memory.  Returns true if successful else false.
 */
bool unload(void);

#endif // DICTIONARY_H
